package operators;

public class postfix_decre_operato {

        public static void main(String[] args) {

            //Postfix increment operator Rule - phle value assign then increment hoga.
            int x=10;
            int y=x--;
            int z=y;

            System.out.println(x);
            System.out.println(y);
            System.out.println(z);
        }
    }
