package operators;

public class LogicalOperator {
    public static void main(String[] args) {

        //jab dono condition true ho tab && True print krta hai
        System.out.println(10<20 && 30<40);

        //jab dono me se koi ek condition true ho to || True print krta hai
        System.out.println(10<20 || 30<40);

    }
}
