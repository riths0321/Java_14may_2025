package operators;

public class ComparisonOperator {
    public static void main(String[] args) {
        int i=10;
        int j=11;

        //this is a comparison operator that compare two numbers
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(i>=j);
        System.out.println(i<=j);

        //this is equality operator that check equality in numbers
        System.out.println(i==j);
        System.out.println(i!=j);
    }
}
