package primitivedatatype;

public class example {

    //byte data type
    byte b = -128;           //1 byte

    //short data type
    short s = -32768;       //2 bytes

    //int data type
    int i =2147483647;      //4 bytes

    //long data type
    long l = 922337299999l;  //8 bytes

    //float data type
    float f= 10.24f;        //4 bytes

    //double data type
    double d= 1111111.11111d;   //8 bytes d optional

    //boolean data type
    boolean boo= true;     //1 byte

    //char data type
    char ch= 'r';      //2 byte //only single character accept


    public static void main(String[] args) {
        System.out.println();
    }
}