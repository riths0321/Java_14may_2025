package conditionalstatement;

public class IfElseStatement {

        public static void main(String[] args) {
            int i=10;
            if(i>=40)
            {
                System.out.println("true");
            }
            else if(i==40)
            {
                System.out.println("ok");
            }
            else
            {
                System.out.println("fail");
        }
    }

}
